function maxFraction(numerators, denominators) {
  return maxNumerators(numerators) / mindenoinator(denominators);
}

function maxNumerators(arr) {
  let max = Math.max(arr);

  return max;
}
function mindenoinator(arr) {
  let min = Math.min(arr);
  return min;
}
console.log(maxFraction([5, 2, 5], [6, 3, 4]));
